import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const MASTER_PATH = path.resolve(__dirname, "../public/data/cities-master.json");
const OUTPUT_PATH = path.resolve(__dirname, "../functions/api/city-aliases.js");

function normalize(value) {
  return String(value || "").trim();
}

function buildAliasRows(cities) {
  const seen = new Set();
  const rows = [];

  for (const city of cities) {
    const slug = normalize(city.slug).toLowerCase();
    const nameZh = normalize(city.nameZh);
    if (!slug || !nameZh) continue;

    const aliases = [nameZh, slug, ...(Array.isArray(city.aliases) ? city.aliases : [])]
      .map(normalize)
      .filter(Boolean);

    for (const alias of aliases) {
      const key = `${alias.toLowerCase()}__${slug}`;
      if (seen.has(key)) continue;
      seen.add(key);
      rows.push({ alias, slug });
    }
  }

  return rows;
}

function toModuleSource(rows) {
  const lines = rows.map(
    (row) => `  { alias: ${JSON.stringify(row.alias)}, slug: ${JSON.stringify(row.slug)} },`
  );
  return `// AUTO-GENERATED by scripts/generate-city-aliases.mjs\n// Source of truth: public/data/cities-master.json\nexport const CITY_ALIASES = [\n${lines.join("\n")}\n];\n`;
}

async function main() {
  const raw = await readFile(MASTER_PATH, "utf8");
  const parsed = JSON.parse(raw);
  const cities = Array.isArray(parsed?.cities) ? parsed.cities : [];
  const rows = buildAliasRows(cities);
  const source = toModuleSource(rows);
  await writeFile(OUTPUT_PATH, source, "utf8");
  console.log(`Generated ${rows.length} aliases -> ${path.relative(process.cwd(), OUTPUT_PATH)}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
